// JavaScript Operators and Expressions


console.log("JavaScript Operators in js")
let a = 44;
let b = 5;
console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a / b = ", a / b)
console.log("a % b = ", a % b)
console.log("a ** b = ", a ** b)
console.log("a++  = ", a++)
console.log("after a++", a)
console.log("a--  = ", a--)
console.log("after a--", a)
console.log("++a  = ", ++a)
console.log("after ++a", a)


